package tech.rsqn.cdsl.context;

public class CustomContextModel {
    private String myModelName;
    private String myModelValue;

    public String getMyModelName() {
        return myModelName;
    }

    public void setMyModelName(String myModelName) {
        this.myModelName = myModelName;
    }

    public String getMyModelValue() {
        return myModelValue;
    }

    public void setMyModelValue(String myModelValue) {
        this.myModelValue = myModelValue;
    }
}
